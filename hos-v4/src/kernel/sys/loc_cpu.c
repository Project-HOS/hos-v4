/* CPUロック状態への移行 */
ER loc_cpu(void)
{
	mknl_loc_sys();	/* システムのロック */

	mknl_loc_cpu();	/* CPUのロック */

	mknl_unl_sys();	/* システムのロック解除 */

	return E_OK;
}


